days=int(input("Enter a number (1-7) day of the week: "))
if days == 1:
    print("Monday")
elif days == 2:     
    print("Tuesday")
elif days == 3:
    print("Wednesday")
elif days == 4:
    print("Thursday")
elif days == 5:
    print("Friday")
elif days == 6:
    print("Saturday")
elif days == 7:
    print("Sunday")
else:
    print("Invalid input Enter a number between 1 and 7.")
